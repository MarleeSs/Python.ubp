# Menentukan Nilai Minimum dan Maksimum dari 2 Bilangan

n1 = 2
n2 = 6

if n1 < n2:
    print(f"{n1} Adalah Nilai >..Minimum..<")
    print(f"{n2} Adalah Nilai <..Maksimum..>")

elif n1 > n2:
    print(f"{n2} Adalah Nilai >..Minimum..<")
    print(f"{n1} Adalah Nilai <..Maksimum..>")

else:
    print(f"{n1} Adalah Nilai >..Minimum..<")
    print(f"{n2} Adalah Nilai <..Maksimum..>")
