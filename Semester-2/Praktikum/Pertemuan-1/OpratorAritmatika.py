print("Operator Aritmatika")

# Variabel a dan b
a = 10
b = 3

# Pengoprasian Aritmatik
Tambah = a + b
print(f"Data {a} + {b} = {Tambah}")

Kurang = a - b
print(f"Data {a} - {b} = {Kurang}")

Kali = a * b
print(f"Data {a} * {b} = {Kali}")

Bagi = a / b
print(f"Data {a} / {b} = {Bagi}")

Modulus = a % b
print(f"Data {a} % {b} = {Modulus}")  # Sisa Bagi

Division = a // b
print(f"Data {a} // {b} = {Division}")  # Dibulatkan Kebawah